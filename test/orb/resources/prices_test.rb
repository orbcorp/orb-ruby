  def test_list
    response = @orb.prices.list

    assert_pattern do
      response => Orb::Internal::Page
    end

    row = response.to_enum.first
    return if row.nil?

    assert_pattern do
      row => Orb::Price
    end

    assert_pattern do
      case row
      in Orb::Price::Unit
      in Orb::Price::Package
      in Orb::Price::Matrix
      in Orb::Price::Tiered
      in Orb::Price::TieredBPS
      in Orb::Price::BPS
      in Orb::Price::BulkBPS
      in Orb::Price::Bulk
      in Orb::Price::ThresholdTotalAmount
      in Orb::Price::TieredPackage
